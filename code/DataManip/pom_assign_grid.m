function [ POM  ] = pom_assign_grid(POM, gX, gY, gZ )

POM.Grid = zeros(gX, gY, gZ);
idx = sub2ind(size(POM.Grid), POM.X, POM.Y, POM.Z);
POM.Grid(idx) = 1:length(POM.X);

end

